    // Boton que devuelve el valor de la raiz, ya sea normal o cubica
    private void jBtnSqrtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnSqrtActionPerformed
        valor1 = Double.parseDouble(jTxtPrincipal.getText());
        
        /*
            Comparamos el texto que está en nuestro boton, esto para poder
            ejecutar una u otra funcion. Si el texto es el de la raiz normal
            ejecuta su codigo, pero si es el de la raiz cubica ejecuta el de la
            raiz cubica.
        */
        if (jBtnSqrt.getText() == "²√x") { // Raiz normal
            if (valor1 >= 0) {
                jTxtSecundario.setText("sqrt(" + valor1 + ")");
                jTxtPrincipal.setText(Math.sqrt(valor1) + "");
            }
        } else if (jBtnSqrt.getText() == "³√x") { // Raiz cubica
            a = 1;
            b = 3;
            c = a / b;
            jTxtSecundario.setText("cuberoot(" + valor1 + ")");
            jTxtPrincipal.setText(Math.pow(valor1, c) + "");
        } else { // En caso de que ninguna de las dos sea validas, arroja error
            jTxtPrincipal.setText("Error");
        }
    }//GEN-LAST:event_jBtnSqrtActionPerformed
    
