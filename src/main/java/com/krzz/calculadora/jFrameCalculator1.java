    /*
        Aqui en esta clase ejecutamos las operaciones dependiendo de la que hayamos
        presionado en el boton, todo mediante un switch case.
    */
    
    private void Operaciones(double valor1, double valor2) {
        switch (tipoOperaciones) { // En caaso de:
            case "+" -> { // Si es una suma, nos dará el resultado de la suma
                resultado = valor1 + valor2;
                jTxtPrincipal.setText(resultado + "");
                valor1 = Double.parseDouble(jTxtPrincipal.getText());
            }
            case "-" -> { // Si es una resta, nos dará el resultado de la resta
                resultado = valor1 - valor2;
                jTxtPrincipal.setText(resultado + "");
                valor1 = Double.parseDouble(jTxtPrincipal.getText());
            }
            case "×" -> { // Si es una multiplicacion, nos dará el resultado de la multiplicacion
                resultado = valor1 * valor2;
                jTxtPrincipal.setText(resultado + "");
                valor1 = Double.parseDouble(jTxtPrincipal.getText());
            }
            case "÷" -> { // Si es una division, nos dará el resultado de la division
                if (valor2 == 0) {
                    jTxtPrincipal.setText("Error");
                } else {
                    resultado = valor1 / valor2;
                    jTxtPrincipal.setText(resultado + "");
                    valor1 = Double.parseDouble(jTxtPrincipal.getText());
                }
            }
            case "potencia" -> { // Si es una potencia, nos dará el resultado de la potencia
                resultado = Math.pow(valor1, valor2);
                jTxtPrincipal.setText(resultado + "");
            }
        }
    }
    
