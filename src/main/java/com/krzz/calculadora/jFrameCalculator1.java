    /*
        Trigonometria
        Aqui hay un comparador if que detecta que RadioButton está seleccionado
        para identificar si son radianes o grados los que está recibiendo, 
        dependiendo de lo que esté seleccionado se ejecuta uno u otro codigo
        mediante un switch case.
    */
    public void Trigonometria() {
        if (jRdbSexages.isSelected()) { // si los grados están seleccionados
            switch (funciones) { // elige uno u otro caso
                // Evalua el seno 
                case "sin":
                    valor1 = Double.parseDouble(jTxtPrincipal.getText());
                    jTxtSecundario.setText("sin(" + valor1 + ")");
                    sin = Math.sin(Math.PI*(valor1)/180);
                    jTxtPrincipal.setText(sin + " ");
                    break;
                // Evalua el seno inverso
                case "asin":
                    valor1 = Double.parseDouble(jTxtPrincipal.getText());
                    jTxtSecundario.setText("sin(" + valor1 + ")");
                    asin = Math.asin((valor1)*180/Math.PI);
                    jTxtPrincipal.setText(asin + " ");
                    break;
                    // Evalua el coseno
                case "cos":
                    valor1 = Double.parseDouble(jTxtPrincipal.getText());
                    jTxtSecundario.setText("cos(" + valor1 + ")");
                    cos = Math.cos(Math.PI*(valor1)/180);
                    jTxtPrincipal.setText(cos + " ");
                    break;
                // Evalua el coseno inverso
                case "acos":
                    valor1 = Double.parseDouble(jTxtPrincipal.getText());
                    jTxtSecundario.setText("acos(" + valor1 + ")");
                    acos = Math.acos((valor1)*180/Math.PI);
                    jTxtPrincipal.setText(acos + " ");
                    break;
                    // Evalua la tangente
                case "tan":
                    valor1 = Double.parseDouble(jTxtPrincipal.getText());
                    jTxtSecundario.setText("tan(" + valor1 + ")");
                    tan = Math.tan(Math.PI*(valor1)/180);
                    jTxtPrincipal.setText(tan + " ");
                    break;
                // Evalua la tangente inversa
                case "atan":
                    valor1 = Double.parseDouble(jTxtPrincipal.getText());
                    jTxtSecundario.setText("atan(" + valor1 + ")");
                    atan = Math.atan((valor1)*180/Math.PI);
                    jTxtPrincipal.setText(atan + " ");
                    break;
            }
        } else {
           if (jRdbRadianes.isSelected()) { // si los radianes están seleccionados
            switch (funciones) { // ejecuta uno u otro caso
                // Evalua el seno
                case "sin":
                    valor1 = Double.parseDouble(jTxtPrincipal.getText());
                    jTxtSecundario.setText("sinr(" + valor1 + ")");
                    sin = Math.sin(valor1);
                    jTxtPrincipal.setText(sin + " ");
                    break;
                // Evalua el seno inverso
                case "asin":
                    valor1 = Double.parseDouble(jTxtPrincipal.getText());
                    jTxtSecundario.setText("sinr(" + valor1 + ")");
                    asin = Math.asin(valor1);
                    jTxtPrincipal.setText(asin + " ");
                    break;
                    // Evalua el coseno
                case "cos":
                    valor1 = Double.parseDouble(jTxtPrincipal.getText());
                    jTxtSecundario.setText("cosr(" + valor1 + ")");
                    cos = Math.cos(valor1);
                    jTxtPrincipal.setText(cos + " ");
                    break;
                // Evalua el coseno inverso
                case "acos":
                    valor1 = Double.parseDouble(jTxtPrincipal.getText());
                    jTxtSecundario.setText("acosr(" + valor1 + ")");
                    acos = Math.acos(valor1);
                    jTxtPrincipal.setText(acos + " ");
                    break;
                    // Evalua la tangente
                case "tan":
                    valor1 = Double.parseDouble(jTxtPrincipal.getText());
                    jTxtSecundario.setText("tanr(" + valor1 + ")");
                    tan = Math.tan(valor1);
                    jTxtPrincipal.setText(tan + " ");
                    break;
                // Evalua la tangente inversa
                case "atan":
                    valor1 = Double.parseDouble(jTxtPrincipal.getText());
                    jTxtSecundario.setText("atanr(" + valor1 + ")");
                    atan = Math.atan(valor1);
                    jTxtPrincipal.setText(atan + " ");
                    break;
            }
        } 
        }
    }
